/// Save SSL/TLS sessions from the shared session cache to a
/// persistence file.
///
/// Iterates over all non-expired sessions in the `ShareHandle`'s TLS
/// session cache, serializes each to the TLV binary format, base64-
/// encodes both the peer key and session data, and writes them as
/// `<base64_key>:<base64_data>` lines to the session file.
///
/// This is the Rust equivalent of `tool_ssls_save()` in
/// `src/tool_ssls.c` (lines 187–213), combined with the
/// `tool_ssls_exp()` callback (lines 138–185).
///
/// # Behavior
///
/// 1. If `config.ssl_sessions_file` is `None`, returns immediately.
/// 2. If the session cache is not available (sharing not enabled),
///    returns immediately.
/// 3. Opens the session file for writing (creates or truncates).
/// 4. Writes the file header comments.
/// 5. For each session in the cache:
///    - Serializes the session to TLV binary format.
///    - Base64-encodes the peer key and session data.
///    - Writes the line `<base64_key>:<base64_data>\n`.
/// 6. Flushes the output buffer to ensure all data is written.
///
/// # Error Handling
///
/// File creation failures produce a warning but do **not** fail the
/// function. Individual session write errors produce per-session
/// warnings but allow remaining sessions to be written. The transfer
/// has already completed at this point, so session save failures do
/// not affect the transfer result.
///
/// # Arguments
///
/// * `global` — Global configuration for message output.
/// * `config` — Operation configuration providing the
///   `ssl_sessions_file` path.
/// * `share` — Shared handle whose session cache is exported.
///
/// # Returns
///
/// * `Ok(())` — Saving completed (possibly with zero sessions).
/// * `Err(...)` — Only on truly unrecoverable errors (should be rare).
///
/// # C Equivalent
///
/// ```c
/// CURLcode tool_ssls_save(struct OperationConfig *config,
///                         CURLSH *share, const char *filename)
/// ```
pub fn ssl_sessions_save(
    global: &GlobalConfig,
    config: &OperationConfig,
    share: &ShareHandle,
) -> Result<()> {
    // Extract the session file path. If not configured, session
    // persistence is disabled — return immediately.
    let filename = match &config.ssl_sessions_file {
        Some(f) => f.as_str(),
        None => return Ok(()),
    };

    // Obtain the shared TLS session cache. If session sharing is not
    // enabled, there is nothing to export.
    let session_cache = match share.get_session_cache() {
        Some(cache) => cache,
        None => return Ok(()),
    };

    // Configure a temporary easy handle (mirrors C tool_ssls_easy()).
    //
    // In the C code, the easy handle is used to call
    // `curl_easy_ssls_export(easy, tool_ssls_exp, &ctx)`.
    // In Rust, we access the session cache directly but create the
    // handle for behavioral parity (debug/trace output configuration).
    let _easy = match configure_session_easy(global) {
        Ok(e) => e,
        Err(e) => {
            // Map CurlError to a warning, matching C behavior where
            // tool_ssls_easy failure is not fatal.
            let curl_err: &CurlError = &e;
            warnf(
                global,
                &format!("Failed to create session helper handle: {}", curl_err),
            );
            return Ok(());
        }
    };

    // Open the session file for writing. This creates the file if it
    // does not exist, or truncates it if it does.
    //
    // C equivalent:
    //   ctx.fp = curlx_fopen(filename, FOPEN_WRITETEXT);
    //   if(!ctx.fp) {
    //     warnf("Warning: Failed to create SSL session file %s", filename);
    //     goto out;
    //   }
    let fp = match File::create(filename) {
        Ok(f) => f,
        Err(e) => {
            warnf(
                global,
                &format!(
                    "Warning: Failed to create SSL session file {}: {}",
                    filename, e
                ),
            );
            return Ok(());
        }
    };

    let mut writer = BufWriter::new(fp);
    let mut exported: usize = 0;

    // Lock the session cache and iterate all stored sessions.
    //
    // C equivalent uses `curl_easy_ssls_export(easy, tool_ssls_exp, &ctx)`
    // which iterates the cache internally and invokes the callback for
    // each session. In Rust, we use `SessionCache::export_all()` to
    // obtain a snapshot of all non-expired sessions.
    if let Ok(cache) = session_cache.lock() {
        let sessions: Vec<(String, TlsSession)> = cache.export_all();

        for (ref peer_key, ref session) in &sessions {
            // Write file header before the first session entry.
            //
            // C equivalent:
            //   if(!ctx->exported)
            //     fputs("# Your SSL session cache...\n"
            //           "# This file was generated by libcurl!...\n", ctx->fp);
            if exported == 0 {
                if writer
                    .write_all(SESSION_FILE_HEADER_1.as_bytes())
                    .is_err()
                {
                    warnf(
                        global,
                        "Warning: error writing SSL session file header",
                    );
                    return Ok(());
                }
                if writer
                    .write_all(SESSION_FILE_HEADER_2.as_bytes())
                    .is_err()
                {
                    warnf(
                        global,
                        "Warning: error writing SSL session file header",
                    );
                    return Ok(());
                }
            }

            // Base64-encode the peer key (shmac).
            //
            // C equivalent: `curlx_base64_encode(shmac, shmac_len, &enc, &enc_len)`
            let key_str: &str = peer_key.as_str();
            let shmac_b64: String = BASE64_ENGINE.encode(key_str.as_bytes());

            // Serialize the session to TLV binary format and base64-encode.
            //
            // C equivalent:
            //   curlx_base64_encode(sdata, sdata_len, &enc, &enc_len)
            let sdata: Vec<u8> = session.serialize();
            let sdata_b64: String = BASE64_ENGINE.encode(&sdata);

            // Write the session line: base64_shmac:base64_sdata\n
            //
            // C equivalent:
            //   fwrite(enc_shmac, 1, enc_len, ctx->fp);
            //   fputc(':', ctx->fp);
            //   fwrite(enc_sdata, 1, enc_len, ctx->fp);
            //   fputc('\n', ctx->fp);
            if writeln!(writer, "{}:{}", shmac_b64, sdata_b64).is_err() {
                warnf(
                    global,
                    &format!(
                        "Warning: error saving SSL session for '{}'",
                        peer_key
                    ),
                );
                // Continue attempting to write remaining sessions rather
                // than aborting — some sessions saved is better than none.
                continue;
            }

            exported += 1;
        }
    } else {
        warnf(
            global,
            "Warning: Failed to lock session cache for export",
        );
        return Ok(());
    }

    // Flush the buffered writer to ensure all data reaches the file.
    if let Err(e) = writer.flush() {
        warnf(
            global,
            &format!(
                "Warning: error flushing SSL session file {}: {}",
                filename, e
            ),
        );
    }

    if exported > 0 {
        notef(
            global,
            &format!(
                "Saved {} SSL session(s) to {}",
                exported, filename
            ),
        );
    }

    Ok(())
}

