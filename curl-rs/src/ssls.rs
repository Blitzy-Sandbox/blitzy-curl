/// Load SSL/TLS sessions from a persistence file into the shared
/// session cache.
///
/// Reads the session file specified by
/// [`OperationConfig::ssl_sessions_file`], parses each line as a
/// base64-encoded `<peer_key>:<session_data>` pair, deserializes the
/// session, and inserts it into the `ShareHandle`'s TLS session cache.
///
/// This is the Rust equivalent of `tool_ssls_load()` in
/// `src/tool_ssls.c` (lines 55–131).
///
/// # Behavior
///
/// 1. If `config.ssl_sessions_file` is `None`, returns immediately
///    (session persistence not configured).
/// 2. If the file does not exist, emits a [`notef`] message and
///    returns `Ok(())` — this is normal on first run.
/// 3. For each line in the file:
///    - Lines starting with `#` or empty lines are skipped.
///    - Lines exceeding [`MAX_SSLS_LINE`] bytes are skipped with a
///      warning.
///    - Lines without a `:` separator are skipped with a warning.
///    - The left field is base64-decoded as the peer key string.
///    - The right field is base64-decoded as serialized session data
///      and deserialized via [`TlsSession::deserialize()`].
///    - Valid sessions are inserted into the shared session cache
///      via [`SessionCache::put()`].
/// 4. If any sessions were successfully imported, emits a [`notef`]
///    message with the count.
///
/// # Error Handling
///
/// File I/O errors, parse errors, and corrupt session data produce
/// warnings but do **not** fail the function. The transfer will
/// proceed without session resumption rather than aborting.
///
/// # Arguments
///
/// * `global` — Global configuration for message output (trace level,
///   silent mode).
/// * `config` — Operation configuration providing the
///   `ssl_sessions_file` path.
/// * `share` — Shared handle whose session cache receives the loaded
///   sessions.
///
/// # Returns
///
/// * `Ok(())` — Loading completed (possibly with zero sessions if file
///   is missing or empty).
/// * `Err(...)` — Only on truly unrecoverable errors (should be rare).
///
/// # C Equivalent
///
/// ```c
/// CURLcode tool_ssls_load(struct OperationConfig *config,
///                         CURLSH *share, const char *filename)
/// ```
pub fn ssl_sessions_load(
    global: &GlobalConfig,
    config: &OperationConfig,
    share: &ShareHandle,
) -> Result<()> {
    // Extract the session file path. If not configured, session
    // persistence is disabled — return immediately.
    let filename = match &config.ssl_sessions_file {
        Some(f) => f.as_str(),
        None => return Ok(()),
    };

    // Attempt to open the session file. Missing files are expected on
    // first run (no prior sessions to restore).
    //
    // C equivalent:
    //   fp = curlx_fopen(filename, FOPEN_READTEXT);
    //   if(!fp) {
    //     notef("SSL session file does not exist (yet?): %s", filename);
    //     goto out;
    //   }
    let fp = match File::open(filename) {
        Ok(f) => f,
        Err(ref e) if e.kind() == std::io::ErrorKind::NotFound => {
            notef(
                global,
                &format!(
                    "SSL session file does not exist (yet?): {}",
                    filename
                ),
            );
            return Ok(());
        }
        Err(ref e) if e.kind() == std::io::ErrorKind::PermissionDenied => {
            warnf(
                global,
                &format!(
                    "Permission denied reading SSL session file: {}",
                    filename
                ),
            );
            return Ok(());
        }
        Err(e) => {
            warnf(
                global,
                &format!(
                    "Failed to open SSL session file {}: {}",
                    filename, e
                ),
            );
            return Ok(());
        }
    };

    // Configure a temporary easy handle (mirrors C tool_ssls_easy()).
    // The handle is used for trace/debug output configuration; actual
    // session import goes through the session cache directly.
    let _easy = match configure_session_easy(global) {
        Ok(e) => e,
        Err(e) => {
            // Map CurlError to a warning message, matching C behavior
            // where tool_ssls_easy failure is not fatal.
            let curl_err: &CurlError = &e;
            warnf(
                global,
                &format!("Failed to create session helper handle: {}", curl_err),
            );
            return Ok(());
        }
    };

    // Obtain the shared TLS session cache. If session sharing is not
    // enabled on the share handle, there is nothing to import into.
    let session_cache = match share.get_session_cache() {
        Some(cache) => cache,
        None => return Ok(()),
    };

    // Read the file line by line using a buffered reader.
    //
    // C equivalent uses `my_get_line(fp, &buf, &error)` with a dynbuf
    // limited to MAX_SSLS_LINE. The Rust BufReader + lines() iterator
    // provides equivalent functionality with automatic memory management.
    let reader = BufReader::new(fp);
    let mut line_num: usize = 0;
    let mut imported: usize = 0;
    let mut read_error = false;

    for line_result in reader.lines() {
        line_num += 1;

        let line = match line_result {
            Ok(l) => l,
            Err(e) => {
                warnf(
                    global,
                    &format!(
                        "Error reading line {} in SSL session file {}: {}",
                        line_num, filename, e
                    ),
                );
                read_error = true;
                break;
            }
        };

        // Skip empty lines and comment lines (header lines start with '#').
        let trimmed = line.trim();
        if trimmed.is_empty() || trimmed.starts_with('#') {
            continue;
        }

        // Enforce maximum line length to prevent excessive memory allocation
        // from malformed or malicious session files.
        //
        // C equivalent: dynbuf is initialized with `curlx_dyn_init(&buf, MAX_SSLS_LINE)`
        if trimmed.len() > MAX_SSLS_LINE {
            warnf(
                global,
                &format!(
                    "Line {} exceeds maximum length in SSL session file {}",
                    line_num, filename
                ),
            );
            continue;
        }

        // Find the colon separator between the peer key (shmac) and
        // the session data (sdata).
        //
        // C equivalent:
        //   c = memchr(line, ':', strlen(line));
        //   if(!c) {
        //     warnf("unrecognized line %d in ssl session file %s", i, filename);
        //     continue;
        //   }
        let colon_pos = match trimmed.find(':') {
            Some(pos) => pos,
            None => {
                warnf(
                    global,
                    &format!(
                        "unrecognized line {} in ssl session file {}",
                        line_num, filename
                    ),
                );
                continue;
            }
        };

        // Split into shmac (peer key) and sdata (session data) portions.
        let shmac_b64 = &trimmed[..colon_pos];
        let sdata_b64 = &trimmed[colon_pos + 1..];

        // Base64-decode the peer key.
        //
        // C equivalent: `curlx_base64_decode(line, &shmac, &shmac_len)`
        let peer_key_bytes = match BASE64_ENGINE.decode(shmac_b64) {
            Ok(bytes) => bytes,
            Err(_) => {
                warnf(
                    global,
                    &format!(
                        "invalid shmac base64 encoding in line {}",
                        line_num
                    ),
                );
                continue;
            }
        };

        // The peer key is a UTF-8 string (hostname:port:config_hash).
        let peer_key = match String::from_utf8(peer_key_bytes) {
            Ok(s) => s,
            Err(_) => {
                warnf(
                    global,
                    &format!(
                        "invalid shmac encoding in line {} (not valid UTF-8)",
                        line_num
                    ),
                );
                continue;
            }
        };

        // Base64-decode the session data.
        //
        // C equivalent: `curlx_base64_decode(line, &sdata, &sdata_len)`
        let sdata = match BASE64_ENGINE.decode(sdata_b64) {
            Ok(bytes) => bytes,
            Err(_) => {
                warnf(
                    global,
                    &format!(
                        "invalid sdata base64 encoding in line {}: {}",
                        line_num, sdata_b64
                    ),
                );
                continue;
            }
        };

        // Deserialize the TLS session from the TLV binary format.
        //
        // C equivalent: `curl_easy_ssls_import(easy, NULL, shmac, shmac_len, sdata, sdata_len)`
        //
        // In the Rust implementation, we deserialize the session using
        // `TlsSession::deserialize()` and insert it directly into the
        // session cache, bypassing the C pattern of going through the
        // easy handle's import API.
        let session = match TlsSession::deserialize(&sdata) {
            Ok(s) => s,
            Err(e) => {
                warnf(
                    global,
                    &format!(
                        "import of session from line {} rejected({})",
                        line_num, e
                    ),
                );
                continue;
            }
        };

        // Insert the deserialized session into the shared session cache.
        //
        // `SessionCache::put()` handles expiry clamping, capacity
        // limits, and TLS-version-specific caching rules (TLS 1.3
        // single-use, TLS 1.2 session reuse).
        if let Ok(mut cache) = session_cache.lock() {
            cache.put(&peer_key, session);
            imported += 1;
        } else {
            warnf(
                global,
                &format!(
                    "Failed to lock session cache for import at line {}",
                    line_num
                ),
            );
        }
    }

    // Report import results.
    if read_error {
        warnf(
            global,
            &format!(
                "Error reading SSL session file {} (imported {} session(s) before error)",
                filename, imported
            ),
        );
    } else if imported > 0 {
        notef(
            global,
            &format!(
                "Loaded {} SSL session(s) from {}",
                imported, filename
            ),
        );
    }

    Ok(())
}

