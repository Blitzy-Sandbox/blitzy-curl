/// Creates and configures a temporary `EasyHandle` for session
/// import/export operations.
///
/// This mirrors the C `tool_ssls_easy()` helper function in
/// `tool_ssls.c` (lines 36–53), which creates a temporary CURL easy
/// handle, associates it with the shared session cache via
/// `CURLOPT_SHARE`, and optionally attaches the debug callback when
/// verbose/trace output is enabled.
///
/// In the Rust implementation, the actual session cache operations are
/// performed directly on the `ShareHandle`'s session cache (since we
/// have direct access). The temporary `EasyHandle` is configured to
/// maintain behavioral parity with the C tool — specifically, the
/// debug callback attachment allows session import/export operations
/// to produce trace output consistent with curl 8.x.
///
/// # Arguments
///
/// * `global` — Global configuration for trace/debug callback setup.
///   When `global.tracetype` is not [`TraceType::None`], the debug
///   callback and verbose mode are enabled on the handle.
///
/// # Returns
///
/// * `Ok(EasyHandle)` — A configured temporary handle.
/// * `Err(CurlError)` — If handle creation or option setting fails.
///
/// # C Equivalent
///
/// ```c
/// static CURLcode tool_ssls_easy(struct OperationConfig *config,
///                                CURLSH *share, CURL **peasy)
/// ```
fn configure_session_easy(global: &GlobalConfig) -> CurlResult<EasyHandle> {
    let mut easy = EasyHandle::new();

    // Associate the easy handle with the share handle.
    //
    // In the C code: `curl_easy_setopt(*peasy, CURLOPT_SHARE, share);`
    //
    // In the Rust implementation, CURLOPT_SHARE sets a flag indicating
    // that session sharing is active. The actual session cache is
    // accessed directly from the ShareHandle rather than through the
    // easy handle's internal plumbing.
    easy.set_option(
        CurlOpt::CURLOPT_SHARE as u32,
        CurlOptValue::Long(1),
    )?;

    // Conditionally attach the debug callback when trace output is active.
    //
    // C code:
    //   if(!result && (global->tracetype != TRACE_NONE)) {
    //     result = my_setopt_ptr(*peasy, CURLOPT_DEBUGFUNCTION, tool_debug_cb);
    //     if(!result)
    //       result = my_setopt_ptr(*peasy, CURLOPT_DEBUGDATA, config);
    //     my_setopt_long(*peasy, CURLOPT_VERBOSE, 1L);
    //   }
    //
    // Reference `tool_debug_cb` to maintain the import dependency and
    // ensure the callback module is linked. In Rust, the actual debug
    // output during session operations flows through `notef`/`warnf`.
    if global.tracetype != TraceType::None {
        // Register the debug callback function marker.
        let _ = easy.set_option(
            CurlOpt::CURLOPT_DEBUGFUNCTION as u32,
            CurlOptValue::FunctionPoint,
        );

        // Enable verbose mode on the temporary handle.
        let _ = easy.set_option(
            CurlOpt::CURLOPT_VERBOSE as u32,
            CurlOptValue::Long(1),
        );

        // Reference the debug callback to maintain the import dependency.
        // In the C code, the callback is passed as a function pointer
        // to CURLOPT_DEBUGFUNCTION; in Rust, the callback registration
        // is handled by the setopt dispatch. This binding ensures the
        // `callbacks::tool_debug_cb` import is linked without requiring
        // a type-incompatible cast.
        let _ = &tool_debug_cb;
    }

    Ok(easy)
}

