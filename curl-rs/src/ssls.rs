// -----------------------------------------------------------------------
// curl-rs/src/ssls.rs — SSL Session Persistence Helpers
//
// Rust rewrite of src/tool_ssls.c and src/tool_ssls.h from curl 8.19.0-DEV.
//
// Implements SSL/TLS session persistence for the curl_share handle,
// allowing session tickets to be saved to and restored from a file
// across transfers. This enables TLS session resumption (0-RTT for
// TLS 1.3, abbreviated handshakes for TLS 1.2) to reduce connection
// latency for repeated transfers to the same host.
//
// # File Format
//
// The session file uses a line-oriented text format compatible with
// curl 8.x `tool_ssls.c`:
//
// ```text
// # Your SSL session cache. https://curl.se/docs/ssl-sessions.html
// # This file was generated by libcurl! Edit at your own risk.
// <base64(peer_key)>:<base64(serialized_session)>
// <base64(peer_key)>:<base64(serialized_session)>
// ...
// ```
//
// Each non-comment, non-empty line contains two base64-encoded fields
// separated by a colon (`:`):
// - **Left field** (`shmac`): The peer key identifier — a composite
//   string encoding the hostname, port, and TLS configuration hash.
//   Base64 encoding matches the C `curlx_base64_encode(shmac, ...)`.
// - **Right field** (`sdata`): The serialized TLS session data in the
//   TLV (Tag-Length-Value) binary format produced by
//   `TlsSession::serialize()`, matching the C `vtls_spack.c` wire
//   format.
//
// # Error Handling
//
// Both functions are designed to be **non-fatal**: file I/O errors,
// parse errors, and corrupt session data produce warning messages via
// [`warnf`] but do **not** abort the transfer. Missing session files
// are reported as informational notes via [`notef`]. This matches the
// C implementation's behavior where `tool_ssls_load` and
// `tool_ssls_save` never return fatal errors that would prevent the
// actual data transfer from proceeding.
//
// # Safety
//
// This module contains **zero** `unsafe` blocks.
//
// SPDX-License-Identifier: curl
// -----------------------------------------------------------------------

