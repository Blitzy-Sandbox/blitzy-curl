/// Maximum line length for an encoded session ticket (64 KiB).
///
/// Matches the C `#define MAX_SSLS_LINE (64 * 1024)` in `tool_ssls.c`.
/// Lines longer than this are rejected during loading to prevent
/// unbounded memory allocation from malformed session files.
const MAX_SSLS_LINE: usize = 64 * 1024;

/// Session file header comment — line 1.
///
/// Written as the first line of the session file before any session
/// data. Matches the C `fputs(...)` in `tool_ssls_exp()`.
const SESSION_FILE_HEADER_1: &str =
    "# Your SSL session cache. https://curl.se/docs/ssl-sessions.html\n";

/// Session file header comment — line 2.
///
/// Written as the second line of the session file. Matches the C
/// `fputs(...)` in `tool_ssls_exp()`.
const SESSION_FILE_HEADER_2: &str =
    "# This file was generated by libcurl! Edit at your own risk.\n";

