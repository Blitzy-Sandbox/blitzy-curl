// ---------------------------------------------------------------------------
// StderrWriter — Write adapter for the global tool_stderr() stream
//
// Wraps the global `Mutex<ToolStderr>` behind the `std::io::Write` trait
// so that `ProgressData.out` can be a `Box<dyn Write>`.  Each `write`
// and `flush` call acquires the mutex, performs the operation, and
// releases it, ensuring thread safety.
// ---------------------------------------------------------------------------

/// A [`Write`] adapter that delegates all output to the global stderr
/// handle returned by [`tool_stderr()`].
///
/// Acquiring the lock on every `write` call is acceptable because the
/// progress callback fires at most 10 times per second.
struct StderrWriter;

impl Write for StderrWriter {
    fn write(&mut self, buf: &[u8]) -> std::io::Result<usize> {
        match tool_stderr().lock() {
            Ok(mut guard) => {
                guard.stream.write_all(buf)?;
                Ok(buf.len())
            }
            Err(_) => Err(std::io::Error::other("stderr mutex poisoned")),
        }
    }

    fn flush(&mut self) -> std::io::Result<()> {
        match tool_stderr().lock() {
            Ok(mut guard) => guard.stream.flush(),
            Err(_) => Err(std::io::Error::other("stderr mutex poisoned")),
        }
    }
}

