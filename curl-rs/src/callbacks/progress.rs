/// Per-transfer progress bar state.
///
/// This is the Rust equivalent of the C `struct ProgressData` from
/// `src/tool_cb_prg.h`.  One instance is allocated per active transfer
/// and tracks all mutable state required to render the progress bar or
/// spinner animation.
///
/// # Fields
///
/// - `calls` — Number of times the callback has been invoked.
/// - `prev` — Previous transfer byte count (for change detection).
/// - `prevtime` — Timestamp of the last redraw (for 10 Hz rate cap).
/// - `width` — Current terminal width in columns (clamped to
///   [`MIN_BARLENGTH`]–[`MAX_BARLENGTH`]).
/// - `out` — Output stream (stderr by default).
/// - `initial_size` — Resume offset added to totals/progress.
/// - `tick` — Spinner animation phase index (0–199).
/// - `bar` — Spinner anchor horizontal position.
/// - `barmove` — Spinner anchor direction (+1 = right, −1 = left).
pub struct ProgressData {
    /// Number of callback invocations for this transfer.
    pub calls: i32,
    /// Previous transfer byte-offset (for detecting stalled progress).
    pub prev: i64,
    /// Monotonic timestamp of the last progress bar redraw.
    pub prevtime: Instant,
    /// Terminal width in columns, clamped to `[MIN_BARLENGTH, MAX_BARLENGTH]`.
    pub width: i32,
    /// Output stream for progress bar rendering (stderr wrapper).
    pub out: Box<dyn Write>,
    /// Resume-from byte offset (from `--continue-at`).
    pub initial_size: i64,
    /// Spinner sine-table phase index (0–199, wrapping).
    pub tick: u32,
    /// Spinner anchor horizontal position within the bar.
    pub bar: i32,
    /// Spinner anchor movement direction (+1 = rightward, −1 = leftward).
    pub barmove: i32,
}

// ---------------------------------------------------------------------------
// fly — Spinner animation for unknown-total transfers
// ---------------------------------------------------------------------------

/// Renders one frame of the spinner animation when the transfer total
/// is unknown.
///
/// Builds a carriage-return line of `bar.width` spaces, overlays the
/// `"-=O=-"` anchor at position `bar.bar`, and places four '#'
/// highlights at sine-wave-derived positions.  Writes the complete
/// frame to `bar.out`, then advances the animation state.
///
/// This function is a direct port of the C `fly()` function from
/// `tool_cb_prg.c` and produces identical output.
///
/// # Arguments
///
/// * `bar`   — Mutable progress state.
/// * `moved` — Whether the transfer byte count changed since the last
///   invocation (controls whether the anchor position advances).
fn fly(bar: &mut ProgressData, moved: bool) {
    let width = bar.width as usize;

    // check = width - 2: range for sine-derived positions
    let check = bar.width - 2;

    // Build frame buffer: '\r' + `width` spaces
    // Total buffer length = 1 (CR) + width
    let buf_len = 1 + width;
    let mut buf = vec![b' '; buf_len];
    buf[0] = b'\r';

    // Place the "-=O=-" anchor at buf[bar.bar + 1]
    let anchor = b"-=O=-";
    let anchor_start = (bar.bar as usize) + 1;
    for (i, &ch) in anchor.iter().enumerate() {
        let pos = anchor_start + i;
        if pos < buf_len {
            buf[pos] = ch;
        }
    }

    // Place '#' highlights at 4 phase-offset sine positions.
    // C formula: pos = sinus[(tick + offset) % 200] / (1000000 / check) + 1
    if check > 0 {
        let divisor = 1_000_000 / check;
        if divisor > 0 {
            for &offset in &[0u32, 5, 10, 15] {
                let idx = ((bar.tick + offset) % 200) as usize;
                let pos = (SINUS[idx] / divisor + 1) as usize;
                if pos < buf_len {
                    buf[pos] = b'#';
                }
            }
        }
    }

    // Write the composed frame to the output stream.
    let _ = bar.out.write_all(&buf);

    // Advance the animation tick by 2 per frame (mod 200).
    bar.tick += 2;
    if bar.tick >= 200 {
        bar.tick -= 200;
    }

    // Move the anchor position if the transfer made progress.
    if moved {
        bar.bar += bar.barmove;
    }

    // Bounce the anchor at the bar boundaries.
    if bar.bar >= (bar.width - 6) {
        bar.barmove = -1;
        bar.bar = bar.width - 6;
    } else if bar.bar < 0 {
        bar.barmove = 1;
        bar.bar = 0;
    }
}

// ---------------------------------------------------------------------------
// update_width — Re-query terminal width
// ---------------------------------------------------------------------------

/// Queries the current terminal width and updates `bar.width`, clamping
/// the result to `[MIN_BARLENGTH, MAX_BARLENGTH]`.
///
/// Direct port of the C `update_width()` function.
fn update_width(bar: &mut ProgressData) {
    let cols = get_terminal_columns() as i32;
    if cols > MAX_BARLENGTH {
        bar.width = MAX_BARLENGTH;
    } else if cols > MIN_BARLENGTH {
        bar.width = cols;
    } else {
        bar.width = MIN_BARLENGTH;
    }
}

// ---------------------------------------------------------------------------
// progressbarinit — Initialise ProgressData for a new transfer
// ---------------------------------------------------------------------------

/// Creates and initialises a [`ProgressData`] for a new transfer.
///
/// Rust equivalent of the C `progressbarinit(bar, config)` function.
/// All fields start at their zero/default state, with the following
/// overrides matching curl 8.x behaviour:
///
/// - `initial_size` is set to `config.resume_from` when
///   `config.use_resume` is `true`.
/// - `tick` starts at 150 (mid-cycle in the sine table).
/// - `barmove` starts at 1 (rightward animation).
/// - `width` is set by querying the terminal.
/// - `out` is wired to the global `tool_stderr` stream.
///
/// # Arguments
///
/// * `config` — The per-operation configuration (provides resume offset).
pub fn progressbarinit(config: &OperationConfig) -> ProgressData {
    let mut bar = ProgressData {
        calls: 0,
        prev: 0,
        prevtime: Instant::now(),
        width: MIN_BARLENGTH, // will be overwritten by update_width
        out: Box::new(StderrWriter),
        initial_size: 0,
        tick: 150,
        barmove: 1,
        bar: 0,
    };

    // Pass the resume-from value through so the progress function can
    // display progress towards the total file, not just the remaining
    // part.
    if config.use_resume {
        bar.initial_size = config.resume_from;
    }

    update_width(&mut bar);

    bar
}

// ---------------------------------------------------------------------------
// tool_progress_cb — CURLOPT_XFERINFOFUNCTION callback
// ---------------------------------------------------------------------------

/// Progress/xferinfo callback for the curl CLI tool.
///
/// This function is the Rust equivalent of the C `tool_progress_cb()`
/// from `tool_cb_prg.c`.  It is registered as the
/// `CURLOPT_XFERINFOFUNCTION` callback and called by the transfer
/// engine on each progress update.
///
/// # Behaviour
///
/// 1. Computes `total` and `point` by adding the resume offset
///    (`initial_size`) to the raw download+upload counters, using
///    saturating arithmetic to avoid overflow.
/// 2. Enforces a 10 Hz (100 ms) redraw rate cap to prevent excessive
///    terminal output.
/// 3. When the total is unknown (`total == 0`), renders a spinner
///    animation via [`fly()`].
/// 4. When the total is known (`total > 0`), renders a hash-bar with
///    percentage display, matching the curl 8.x output format exactly:
///    `\r{hashes…}{spaces…} {percent:5.1}%`
/// 5. Clears `config.readbusy` when set (the caller is responsible for
///    unpausing the transfer, since the curl handle is not available
///    in this scope).
///
/// # Arguments
///
/// * `bar`     — Mutable progress bar state for this transfer.
/// * `config`  — Mutable per-operation configuration (for readbusy).
/// * `dltotal` — Expected total download size reported by the server.
/// * `dlnow`   — Bytes downloaded so far.
/// * `ultotal` — Expected total upload size.
/// * `ulnow`   — Bytes uploaded so far.
///
/// # Returns
///
/// Always returns `0` (continue transfer).  A non-zero return would
/// abort the transfer, which this callback never does.
pub fn tool_progress_cb(
    bar: &mut ProgressData,
    config: &mut OperationConfig,
    dltotal: i64,
    dlnow: i64,
    ultotal: i64,
    ulnow: i64,
) -> i32 {
    let now = Instant::now();

    // ------------------------------------------------------------------
    // Calculate expected transfer size.
    //
    // `initial_size` can be negative when indicating that we are
    // expecting to get the filesize from the remote.
    // ------------------------------------------------------------------
    let total: i64;
    let point: i64;

    if bar.initial_size < 0 {
        // Negative initial_size: waiting for remote to supply file size.
        if dltotal != 0 || ultotal != 0 {
            total = dltotal.saturating_add(ultotal);
        } else {
            total = i64::MAX;
        }
    } else {
        let combined_total = dltotal.saturating_add(ultotal);
        if (i64::MAX - bar.initial_size) < combined_total {
            total = i64::MAX;
        } else {
            total = combined_total.saturating_add(bar.initial_size);
        }
    }

    // ------------------------------------------------------------------
    // Calculate the current progress.
    // ------------------------------------------------------------------
    if bar.initial_size < 0 {
        if dltotal != 0 || ultotal != 0 {
            point = dlnow.saturating_add(ulnow);
        } else {
            point = i64::MAX;
        }
    } else {
        let combined_now = dlnow.saturating_add(ulnow);
        if (i64::MAX - bar.initial_size) < combined_now {
            point = i64::MAX;
        } else {
            point = combined_now.saturating_add(bar.initial_size);
        }
    }

    // ------------------------------------------------------------------
    // Rate limiting — enforce 10 Hz redraw cap after the first call.
    // ------------------------------------------------------------------
    if bar.calls != 0 {
        // After the first call...
        if total != 0 {
            // We know the total data to transfer.
            if bar.prev == point {
                // Progress did not change since last invocation.
                return 0;
            } else if now.duration_since(bar.prevtime) < REDRAW_INTERVAL
                && point < total
            {
                // Limit progress-bar updating to 10 Hz except at 100%.
                return 0;
            }
        } else {
            // Total is unknown.
            if now.duration_since(bar.prevtime) < REDRAW_INTERVAL {
                // Limit spinner updating to 10 Hz.
                return 0;
            }
            update_width(bar);
            fly(bar, point != bar.prev);
        }
    }

    // Simply count invocations.
    bar.calls += 1;

    update_width(bar);

    // ------------------------------------------------------------------
    // Render the percentage hash-bar when total > 0 and progress changed.
    // ------------------------------------------------------------------
    if total > 0 && point != bar.prev {
        // If we've received more than the expected total, adjust.
        let effective_total = if point > total { point } else { total };

        let frac = (point as f64) / (effective_total as f64);
        let percent = frac * 100.0;
        let barwidth = (bar.width - 7) as usize;

        let mut num = ((barwidth as f64) * frac) as usize;
        if num > MAX_BARLENGTH as usize {
            num = MAX_BARLENGTH as usize;
        }

        // Build the hash string (left-justified in a field of barwidth).
        let hashes: String = "#".repeat(num);

        // Format matching curl 8.x: \r%-{barwidth}s %5.1f%%
        //   - '\r'
        //   - left-justified hash+space field of barwidth chars
        //   - one space
        //   - 5-wide percentage with 1 decimal place
        //   - literal '%'
        let line = format!(
            "\r{:<barwidth$} {:5.1}%",
            hashes,
            percent,
            barwidth = barwidth,
        );
        let _ = bar.out.write_all(line.as_bytes());
    }

    // Flush the output stream to ensure the bar is visible.
    let _ = bar.out.flush();

    // Record the latest progress point and timestamp.
    bar.prev = point;
    bar.prevtime = now;

    // Handle readbusy flag: clear it so the caller knows to unpause
    // the transfer.  The curl_easy_pause(CURLPAUSE_CONT) call is the
    // responsibility of the caller (operate.rs), which has access to
    // the transfer handle.
    if config.readbusy {
        config.readbusy = false;
    }

    0
}

